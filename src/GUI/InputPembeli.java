    public void showTblPembeli() throws SQLException {
       DefaultTableModel tbPembeli = new DefaultTableModel(new String[] {"Nama Pembeli","No Telp"},0);
       for(Pembeli p : transaksi.getDataPembeli()){
           tbPembeli.addRow(new String[] {p.getNAMA_PEMBELI().toString(), p.getNO_TELP().toString()});
       }
       TblPembeli.setModel(tbPembeli);
    }

