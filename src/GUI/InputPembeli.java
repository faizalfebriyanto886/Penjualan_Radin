    private void txtInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtInsertActionPerformed
      
        try {
            Pembeli p = new Pembeli();
            p.setNAMA_PEMBELI(txtNmPembeli.getText());
            p.setNO_TELP(txtNoTelp.getText());
            
            transaksi.InsertPembeli(p);
            showTblPembeli();
            
        } catch (SQLException ex) {
            Logger.getLogger(InputPembeli.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_txtInsertActionPerformed

